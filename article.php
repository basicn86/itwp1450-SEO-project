    <p>Stocks, also known as shares or equities, represent ownership in a publicly traded company. When you buy a stock, you essentially become a part owner of that company and are entitled to a portion of its assets and profits.
Companies issue stocks as a way to raise capital, which can be used for growth, research and development, or other business expenses. Investors buy stocks in the hopes of making a profit, either through increases in the stock price or through dividends, which are payments made by the company to its shareholders.
Stocks can be bought and sold on stock exchanges, such as the New York Stock Exchange (NYSE) or Nasdaq, or over-the-counter (OTC) markets. The price of a stock is determined by supply and demand, and can be influenced by a variety of factors, such as the company's financial performance, news events, and broader economic trends.
Investing in stocks can be a way to build wealth over the long term, but it also carries risks, as the value of a stock can fluctuate widely based on market conditions and company performance. It is important to do your own research and consult with a financial professional before making any investment decisions.</p>

